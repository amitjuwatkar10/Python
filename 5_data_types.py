#Python Data Types

number  =  10 #Numeric
print(type(number))

message = "I love Mumbai" #String 
print(type(message))

cities = ["Delhi", "Mumbai", "Banglore"] #Sequence	List 
print(type(cities))

states = ("Maharashtra", "West Bengal", "Kolkata", "Gujarat") #Sequence	Tuples 
print(type(states))

studentDetails  = { 'rollno': 1, "name":"Amit", "class":"FY"} #Mapping	dictionary 
print(type(studentDetails))

status = True #boolean 
print(type(status))

unique = { 1,2,4,5,6,32,4,2,11,1,1,1,1,1}
print(type(unique))

